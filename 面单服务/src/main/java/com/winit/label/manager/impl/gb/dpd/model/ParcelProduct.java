package com.winit.label.manager.impl.gb.dpd.model;

public class ParcelProduct
{
	protected String	countryOfOrigin;
	protected int		numberOfItems;
	protected String	productCode;
	protected String	productFabricContent;
	protected String	productHarmonisedCode;
	protected String	productItemsDescription;
	protected String	productTypeDescription;
	protected float		unitValue;
	protected float		unitWeight;
	public String getCountryOfOrigin()
	{
		return countryOfOrigin;
	}
	public void setCountryOfOrigin(String countryOfOrigin)
	{
		this.countryOfOrigin = countryOfOrigin;
	}
	public int getNumberOfItems()
	{
		return numberOfItems;
	}
	public void setNumberOfItems(int numberOfItems)
	{
		this.numberOfItems = numberOfItems;
	}
	public String getProductCode()
	{
		return productCode;
	}
	public void setProductCode(String productCode)
	{
		this.productCode = productCode;
	}
	public String getProductFabricContent()
	{
		return productFabricContent;
	}
	public void setProductFabricContent(String productFabricContent)
	{
		this.productFabricContent = productFabricContent;
	}
	public String getProductHarmonisedCode()
	{
		return productHarmonisedCode;
	}
	public void setProductHarmonisedCode(String productHarmonisedCode)
	{
		this.productHarmonisedCode = productHarmonisedCode;
	}
	public String getProductItemsDescription()
	{
		return productItemsDescription;
	}
	public void setProductItemsDescription(String productItemsDescription)
	{
		this.productItemsDescription = productItemsDescription;
	}
	public String getProductTypeDescription()
	{
		return productTypeDescription;
	}
	public void setProductTypeDescription(String productTypeDescription)
	{
		this.productTypeDescription = productTypeDescription;
	}
	public float getUnitValue()
	{
		return unitValue;
	}
	public void setUnitValue(float unitValue)
	{
		this.unitValue = unitValue;
	}
	public float getUnitWeight()
	{
		return unitWeight;
	}
	public void setUnitWeight(float unitWeight)
	{
		this.unitWeight = unitWeight;
	}
	
	
}
