package com.winit.label.manager.impl.gb.dpd.model;

public class ConsignmentDetail
{
	protected String consignmentNumber;
	protected String[] parcelNumbers;
	public String getConsignmentNumber()
	{
		return consignmentNumber;
	}
	public void setConsignmentNumber(String consignmentNumber)
	{
		this.consignmentNumber = consignmentNumber;
	}
	public String[] getParcelNumbers()
	{
		return parcelNumbers;
	}
	public void setParcelNumbers(String[] parcelNumbers)
	{
		this.parcelNumbers = parcelNumbers;
	}
	
	
}
